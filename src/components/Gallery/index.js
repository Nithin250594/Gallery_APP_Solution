class Gallery extends Component {
  state = {galleryImage: imagesList[0]}

  thumbnailClick = uniqueId => {
    this.setState({galleryImage: imagesList[uniqueId]})
  }

  render() {
    const {galleryImage} = this.state
    const {imageUrl, imageAltText} = galleryImage

    console.log({galleryImage})
    return (
      <div className="gallery-bg">
        <img src={imageUrl} alt={imageAltText} className="gallery-img" />
        <h1 className="gallery-title">Nature Photography</h1>
        <p className="gallery-subtitle">Nature Photography by Rahul</p>
        <ul className="Gallery-list-container">
          {imagesList.map(eachGalleryItem => (
            <ThumbnailItem
              key={eachGalleryItem.id}
              thumbnailDetails={eachGalleryItem}
              thumbnailClick={this.thumbnailClick}
              isActive={eachGalleryItem.id === galleryImage.id}
            />
          ))}
        </ul>
      </div>
    )
  }
}

